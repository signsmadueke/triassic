                        <p class="mb-30 mt-10">It started many years ago with Apple's foray into the world of digital devices and entertainment. I remember with much fun my Sony Walkman - this cassette (another artifact from the years of yore) player that was considered handy at that time and the one option for mobile music, probably the only option at the time. It had a radio function that was not much use, but being able to listen to and share music while walking to the store or jogging was fun.<br><br>Then along came Apple. I read awhile back about the journey by Steve Jobs into Japan and how that one trip birthed the introduction of the Apple iPod. Not much further behind was the iPhone, the many iterations or generations of which has become the mainstay of the Apple Corporation since 9 January 2007, when Steve Jobs held up the new iPhone in front of the Apple faithful in the Moscone Centre, San Francisco and thereby launched the app economy.<br><br>One of the commercial and legal hallmarks of the Apple devices are the fact that they have become de-facto standards for upsetting the market. Consider the fact that they introduced devices to the market where you had no control over the software on the devices - you simply take it as delivered, and 'jailbreaking' - that term that fits more with Alcatraz than iOS - meant you could invalidate whatever warranties were in effect on the device, or more interestingly the fact that the batteries were non-removable; that software releases were structures and frequent with each introducing not just security and performance fixes but new features as well. These measure firstly introduced by Apple to the chagrin of many and the delight of many others (majority of Apple customers love the devices because they simply work, and they work simply!) gave them leverage and a head start in the mobile devices market. Oh, and ingenuity as well.<br><br>Over the past 5 years, we have seen the entry of Oracle's Engineered Systems into the Data Center, and are they rolling out like iPhones! Maybe not exactly in the same numbers, but the promise of systems that work simply and simply work is a compelling case to any CIO. The members of the Engineered System range of products (Exadata, Exalogic, SuperCluster, Database Appliance, and may more) come pre-installed and pre-configured with all the necessary checks completed by Oracle engineers. This reduces quite considerably the deployment times between order and commissioning, but more importantly deliver measured assurance to the CIO that the systems work as promised. This in consideration that much of the deployment time and cost comes from different teams working to deliver services rather than simply a solution.</p>
